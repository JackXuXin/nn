	getGameConfig = function (self, gameId)
		for _,v in ipairs(self) do
			if v.gameid == gameId then
				return v
			end
		end
		return nil
	end,

	getRoomConfig = function (self, gameId, roomId)
		local gameConfig = self:getGameConfig(gameId)
		if gameConfig and gameConfig.room then
			for _,v in ipairs(gameConfig.room) do
				if v.roomid == roomId then
					return v
				end
			end
		end
		return nil
	end,

	getFullName = function (self, roomId)
		for _, v in ipairs(self) do 
			for _, r in pairs(v.room) do
				if r.roomid == roomId then
					return v.name .. '-' .. r.name
				end
			end
		end
		return nil
	end,
	getGameNameWithGameId = function (self, gameId)
		for _,v in ipairs(self) do
			if v.gameid == gameId then
				return v.name
			end
		end
		return nil
	end,
}

